    private void btnBuscarCompraActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarCompraActionPerformed

        String IDfornecimento = txtIDfornecimentoCompraBusca.getText(); // pegando os dados do ID do fornecimento digitado no respectivo campo de busca.

        // Verificar se o id fornecimento foi digitado
        if (!IDfornecimento.isEmpty()) {
            ComprasDAO compradao = new ComprasDAO();
            ComprasDTO compradto = compradao.buscarCompraPorID(IDfornecimento);

            // Verificar se encontrou a compra
            if (compradto != null) {
                // A compra foi encontrada, desta forma ele seta os valores dos campos com os dados da respectiva compra.

                txtCpfFCompraBusca.setText(compradto.getCPFfuncionario());
                txt_identificadorProdutoCompraB.setText(compradto.getCodigoIdentificadorProduto());
                txtCnpjFCompraBusca1.setText(compradto.getCNPJfornecedor());
                txtQTDcompraBusca.setText(compradto.getQuantidade());

                JOptionPane.showMessageDialog(null, "Compra encontrada!");
            } else {
                // O funcionário não foi encontrado
                JOptionPane.showMessageDialog(null, "Compra não encontrada!");
            }
        } else {
            // O ID de fornecimento não foi digitado
            JOptionPane.showMessageDialog(null, "Digite um ID de Fornecimento válido!");
        }


    }//GEN-LAST:event_btnBuscarCompraActionPerformed

