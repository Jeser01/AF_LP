    private void btnCadastrarProdutoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCadastrarProdutoActionPerformed

        String CPFfuncionario, CodigoIdentificadorProduto, CNPJfornecedor, IDfornecimento, quantidadeCompra;

        CPFfuncionario = cbxFuncionarios1.getSelectedItem().toString();
        CodigoIdentificadorProduto = cbxProduto.getSelectedItem().toString();
        IDfornecimento = txtIDfornecimentoCompra.getText();
        quantidadeCompra = txtQTDprodutoCompra.getText();
        CNPJfornecedor = txtCnpjFCompra.getText();

        ComprasDTO novaCompra = new ComprasDTO();
        novaCompra.setCPFfuncionario(CPFfuncionario);
        novaCompra.setCNPJfornecedor(CNPJfornecedor);
        novaCompra.setIDfornecimento(IDfornecimento);
        novaCompra.setQuantidade(quantidadeCompra);
        novaCompra.setCodigoIdentificadorProduto(CodigoIdentificadorProduto);

        ComprasDAO novaCompradao = new ComprasDAO();
        boolean cadastrado = novaCompradao.cadastrarCompra(novaCompra);

        if (cadastrado) {
            JOptionPane.showMessageDialog(this, "Compra cadastrada com sucesso!");
        } else {
            JOptionPane.showMessageDialog(this, "Erro ao cadastrar Compra. Verifique os dados e tente novamente.");
        }


    }//GEN-LAST:event_btnCadastrarProdutoActionPerformed

