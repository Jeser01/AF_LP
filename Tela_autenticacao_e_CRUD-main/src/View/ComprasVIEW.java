    private void btnAtualizarCompraActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAtualizarCompraActionPerformed

        String IDfornecimento = txtIDfornecimentoCompraBusca.getText(); // Obtém o ID fornecimento digitado pelo usuário

        ComprasDAO compradao = new ComprasDAO();

// Busca a compra pelo id
        ComprasDTO compradto = compradao.buscarCompraPorID(IDfornecimento);

        if (compradto != null) {
            // Aqui você pode preencher os campos do formulário com os dados da compra, caso deseje

            // Atualiza os campos necessários no objeto ComprasDTO
            compradto.setQuantidade(txtQTDcompraBusca.getText());
            compradto.setCNPJfornecedor(txtCnpjFCompraBusca1.getText());
            compradto.setCodigoIdentificadorProduto(txt_identificadorProdutoCompraB.getText());
            compradto.setCPFfuncionario(txtCpfFCompraBusca.getText());
            compradto.setIDfornecimento(IDfornecimento);
            

            // Realiza a atualização da compra
            boolean atualizado = compradao.atualizarCompra(compradto);

            if (atualizado) {
                JOptionPane.showMessageDialog(this, "Compra atualizada com sucesso!");
            } else {
                JOptionPane.showMessageDialog(this, "Erro ao atualizar compra. Verifique os dados e tente novamente.");
            }
        } else {
            JOptionPane.showMessageDialog(this, "Compra não encontrada. Verifique o ID de fornecimento e tente novamente.");
        }


    }//GEN-LAST:event_btnAtualizarCompraActionPerformed

