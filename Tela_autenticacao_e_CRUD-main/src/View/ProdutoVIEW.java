    private void btnExcluirProdutoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnExcluirProdutoActionPerformed

        String codigo_identificador_produto = txtCod_identificadorProdutoBusca.getText();

        ProdutoDAO produtodao = new ProdutoDAO();

        boolean excluido = produtodao.excluirProdutoPorCODidentificador(codigo_identificador_produto);

        if (excluido) {
            JOptionPane.showMessageDialog(this, "Produto excluido com sucesso.");
        } else {
            JOptionPane.showMessageDialog(this, "Falha ao excluir Produto, "
                    + "verifique o código Identificador e tente novamente.");
        }
    }//GEN-LAST:event_btnExcluirProdutoActionPerformed

    private void btnAtualizarProdutoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAtualizarProdutoActionPerformed

        String codigo_identificador_produto = txtCod_identificadorProdutoBusca.getText();
        ProdutoDAO produtodao = new ProdutoDAO();

        // Busca o produto pelo Código Identificador
        ProdutoDTO produtodto = produtodao.buscarProduto(codigo_identificador_produto);

        if (produtodto != null) {
            // Atualizar dados do fornecedor

            // Após atualizar os campos necessários, você pode criar um objeto Funcionario com os dados atualizados
            ProdutoDTO produtoAtualizado = new ProdutoDTO();

            produtoAtualizado.setCodigo_identificador_produto(codigo_identificador_produto);
            produtoAtualizado.setNome_produto(txtNomeProdutoBusca.getText());
            produtoAtualizado.setCategoria_produto(txtCategoriaProdutoBusca.getText());
            produtoAtualizado.setMarca_produto(txtMarcaProdutoBusca.getText());
            produtoAtualizado.setPreco_produto(txtprecoProdutoBusca.getText());
            produtoAtualizado.setCnpj_fornecedor(txtCnpjFProdutoBusca.getText());
            produtoAtualizado.setQtd_Produto(txtQTDprodutoBusca.getText());
            // ...
            // Realiza a atualização do funcionário
            boolean atualizado = produtodao.atualizarProduto(produtoAtualizado);

            if (atualizado) {
                JOptionPane.showMessageDialog(this, "Produto atualizado com sucesso!");
            } else {
                JOptionPane.showMessageDialog(this, "Erro ao atualizar Produto. Verifique os dados e tente novamente.");
            }
        } else {
            JOptionPane.showMessageDialog(this, "Produto não encontrado. Verifique o Código identificador e tente novamente.");
        }
    }

