    private void btnBuscarProdutoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarProdutoActionPerformed

        String codigo_identificador_produto = txtCod_identificadorProdutoBusca.getText();

        // Verificar se o Código identificador foi digitado
        if (!codigo_identificador_produto.isEmpty()) {
            ProdutoDAO produtoDAO = new ProdutoDAO();
            ProdutoDTO produtoEncontrado = produtoDAO.buscarProduto(codigo_identificador_produto);

            // Verificar se encontrou o ptoduto
            if (produtoEncontrado != null) {
                // O produto foi encontrado, desta forma ele seta os valores dos campos com os dados do respectivo funcionario.
                txtNomeProdutoBusca.setText(produtoEncontrado.getNome_produto());
                txtCategoriaProdutoBusca.setText(produtoEncontrado.getCategoria_produto());
                txtMarcaProdutoBusca.setText(produtoEncontrado.getMarca_produto());
                txtprecoProdutoBusca.setText(produtoEncontrado.getPreco_produto());
                txtCnpjFProdutoBusca.setText(produtoEncontrado.getCnpj_fornecedor());
                txtQTDprodutoBusca.setText(produtoEncontrado.getQtd_Produto());

                JOptionPane.showMessageDialog(null, "Produto encontrado!");
            } else {
                // O produto não foi encontrado
                JOptionPane.showMessageDialog(null, "Produto não encontrado!");
            }
        } else {
            // O código identificador não foi digitado
            JOptionPane.showMessageDialog(null, "Digite um Código Identificador válido!");
        }


    }//GEN-LAST:event_btnBuscarProdutoActionPerformed

