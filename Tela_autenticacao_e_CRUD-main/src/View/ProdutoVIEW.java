    private void btnCadastrarProdutoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCadastrarProdutoActionPerformed

        String codigo_identificador_produto, nome_produto, categoria_produto, marca_produto, preco_produto, cnpj_fornecedor, qtd_produto;

        codigo_identificador_produto = txtCODprodutoIdentificador.getText();
        nome_produto = txtNomeProduto.getText();
        categoria_produto = txtCategoriaProduto.getText();
        marca_produto = txtMarcaProduto.getText();
        preco_produto = txtPrecoProduto.getText();
        cnpj_fornecedor = txtCnpjFProduto.getText();
        qtd_produto = txtQTDproduto.getText();

        ProdutoDTO novoProduto = new ProdutoDTO();

        novoProduto.setCodigo_identificador_produto(codigo_identificador_produto);
        novoProduto.setNome_produto(nome_produto);
        novoProduto.setCategoria_produto(categoria_produto);
        novoProduto.setMarca_produto(marca_produto);
        novoProduto.setPreco_produto(preco_produto);
        novoProduto.setCnpj_fornecedor(cnpj_fornecedor);
        novoProduto.setQtd_Produto(qtd_produto);

        ProdutoDAO novoProdutoDAO = new ProdutoDAO();
        boolean cadastrado = novoProdutoDAO.cadastrarProduto(novoProduto);

        if (cadastrado) {
            JOptionPane.showMessageDialog(this, "Produto cadastrado com sucesso!");
        } else {
            JOptionPane.showMessageDialog(this, "Erro ao cadastrar produto. Verifique os dados e tente novamente.");
        }


    }//GEN-LAST:event_btnCadastrarProdutoActionPerformed

