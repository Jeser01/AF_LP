    private void btnBuscarFornecedorActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarFornecedorActionPerformed

        String cnpjFornecedor = txtCnpjFornecedorBusca.getText();

        // Verificar se o cnpj foi digitado
        if (!cnpjFornecedor.isEmpty()) {
            FornecedorDAO fornecedordao = new FornecedorDAO();
            Fornecedor fornecedor = fornecedordao.buscarFornecedorPorCnpj(cnpjFornecedor);

            // Verificar se encontrou o fornecedor
            if (fornecedor != null) {
                // O fornecedor foi encontrado, desta forma ele seta os valores dos campos com os dados do respectivo funcionario.
                txtCnpjFornecedorBusca.setText(fornecedor.getCnpjFornecedor());
                txtNomeFornecedorBusca.setText(fornecedor.getNomeFornecedor());
                txtTELfornecedorBusca.setText(fornecedor.getTelefoneFornecedor());
                txtEmailFornecedorBusca.setText(fornecedor.getEmailFornecedor());
                txtEnderecoFornecedorBusca.setText(fornecedor.getEnderecoFornecedor());
                txtCidadeFornecedorBusca.setText(fornecedor.getCidadeFornecedor());
                txtEstadoFornecedorBusca.setText(fornecedor.getEstadoFornecedor());

                JOptionPane.showMessageDialog(null, "Fornecedor encontrado!");
            } else {
                // O funcion√°rio n√£o foi encontrado
                JOptionPane.showMessageDialog(null, "Fornecedor n√£o encontrado!");
            }
        } else {
            // O CNPJ n√£o foi digitado
            JOptionPane.showMessageDialog(null, "Digite um CNPJ v√°lido!");
        }


    }//GEN-LAST:event_btnBuscarFornecedorActionPerformed

    private void jButonAtualizarFornecedorActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButonAtualizarFornecedorActionPerformed

        String CnpjFornecedor = txtCnpjFornecedorBusca.getText();
        FornecedorDAO fornecedorDAO = new FornecedorDAO();

        // Busca o funcion√°rio pelo CPF
        Fornecedor fornecedor = fornecedorDAO.buscarFornecedorPorCnpj(CnpjFornecedor);

        if (fornecedor != null) {
            // Atualizar dados do fornecedor

            // Ap√≥s atualizar os campos necess√°rios, voc√™ pode criar um objeto Funcionario com os dados atualizados
            Fornecedor fornecedorAtualizado = new Fornecedor();
            
            fornecedorAtualizado.setCnpjFornecedor(CnpjFornecedor);
            fornecedorAtualizado.setNomeFornecedor(txtNomeFornecedorBusca.getText());
            fornecedorAtualizado.setTelefoneFornecedor(txtTELfornecedorBusca.getText());
            fornecedorAtualizado.setEmailFornecedor(txtEmailFornecedorBusca.getText());
            fornecedorAtualizado.setEnderecoFornecedor(txtEnderecoFornecedorBusca.getText());
            fornecedorAtualizado.setCidadeFornecedor(txtCidadeFornecedorBusca.getText());
            fornecedorAtualizado.setEstadoFornecedor(txtEstadoFornecedorBusca.getText());

            // ...
            // Realiza a atualiza√ß√£o do funcion√°rio
            boolean atualizado = fornecedorDAO.atualizarFornecedor(fornecedorAtualizado);

            if (atualizado) {
                JOptionPane.showMessageDialog(this, "Fornecedor atualizado com sucesso!");
            } else {
                JOptionPane.showMessageDialog(this, "Erro ao atualizar Fornecedor. Verifique os dados e tente novamente.");
            }
        } else {
            JOptionPane.showMessageDialog(this, "Fornecedor n√£o encontrado. Verifique o CNPJ e tente novamente.");
        }
    }//GEN-LAST:event_jButonAtualizarFornecedorActionPerformed

