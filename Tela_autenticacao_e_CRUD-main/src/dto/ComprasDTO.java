
package dto;


public class ComprasDTO {
    
    private String IDfornecimento;
    private String CPFfuncionario;
    private String CodigoIdentificadorProduto;
    private String quantidade;
    private String CNPJfornecedor;

    public String getIDfornecimento() {
        return IDfornecimento;
    }

    public void setIDfornecimento(String IDfornecimento) {
        this.IDfornecimento = IDfornecimento;
    }

    public String getCPFfuncionario() {
        return CPFfuncionario;
    }

    public void setCPFfuncionario(String CPFfuncionario) {
        this.CPFfuncionario = CPFfuncionario;
    }

    public String getCodigoIdentificadorProduto() {
        return CodigoIdentificadorProduto;
    }

    public void setCodigoIdentificadorProduto(String CodigoIdentificadorProduto) {
        this.CodigoIdentificadorProduto = CodigoIdentificadorProduto;
    }

    public String getQuantidade() {
        return quantidade;
    }

    public void setQuantidade(String quantidade) {
        this.quantidade = quantidade;
    }

    public String getCNPJfornecedor() {
        return CNPJfornecedor;
    }

    public void setCNPJfornecedor(String CNPJfornecedor) {
        this.CNPJfornecedor = CNPJfornecedor;
    }
    
            
    
}
